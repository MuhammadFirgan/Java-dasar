package com.OpsAritmatika;

public class Main {

  public static void main(String[] args) {
	int var1;
	int var2;
	var1 = 10;
	var2 = 5;
	
	//penjumlahan
	
	int hasil;
	hasil = var1 + var2;
	System.out.println(var1 + " + " + var2 + " = " + hasil);
	
	//pengurangan
	hasil = var1 - var2;
	System.out.printf("%d - %d = %d \n",var1,var2,hasil);
	
	//perkalian
	hasil = var1 * var2;
	System.out.printf("%d * %d = %d",var1,var2,hasil);
  }
}
